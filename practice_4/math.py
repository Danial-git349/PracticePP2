# Code #2: Math Examples

import math
import random

print("=== Math Examples ===")

# Example 1: Square root
print("Example 1 - sqrt(25):", math.sqrt(25))

# Example 2: Power
print("Example 2 - 2^3:", math.pow(2, 3))

# Example 3: Ceiling & Floor
print("Example 3 - ceil(4.2):", math.ceil(4.2), "floor(4.8):", math.floor(4.8))

# Example 4: Random numbers
print("Example 4 - randint(1,10):", random.randint(1,10), "random():", random.random())