# Casting - 5 beginner examples

# Example 1: int to float
x = float(5)
print(x, type(x))

# Example 2: float to int
y = int(3.99)
print(y, type(y))

# Example 3: str to int
z = int("10")
print(z, type(z))

# Example 4: str to float
f = float("3.14")
print(f, type(f))

# Example 5: int to str
n = str(123)
print(n, type(n))
